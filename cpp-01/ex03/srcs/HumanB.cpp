#include <string>
#include <iostream>
#include "HumanB.hpp"
#include "Weapon.hpp"

/* ************************************************************************** */
/*                          CONSTRUCTOR & DESTRUCTOR                          */
/* ************************************************************************** */

HumanB::HumanB(std::string name) : _name(name), _weapon(NULL) {}

HumanB::~HumanB(void) {}

/* ************************************************************************** */
/*                              PUBLIC FUNCTIONS                              */
/* ************************************************************************** */

void	HumanB::attack(void)
{
	std::cout 	<< this->_name << " attacks with their " << this->_weapon->getType()
				<< std::endl;
}

/* ************************************************************************** */
/*                                SETTERS    	                              */
/* ************************************************************************** */

void	HumanB::setWeapon(Weapon& weapon)
{
	this->_weapon = &weapon;
}